                        根据数据商Markit编制的报告，主要经济体的1月采购经理人指数（PMI）显示全球经济增长在今年初获得动力。摩根大通1月全球PMI指数从去年12月的53.6上升至53.9，为22个月高位，并且已经连续第五个月上升。
