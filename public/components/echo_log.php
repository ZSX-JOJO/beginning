                    <h3 class="log-title">世界经济不理会悲观警讯：PMI一片“繁荣”</h3>
                    <div class="log-info">
                        <i class="iconfont icon-user"></i>
                        <a href="">jaeheng</a>
                        <i class="iconfont icon-time"></i> 2017-2-9 12:10:12
                        <i class="iconfont icon-comment"></i> <a href="#comments">345</a>
                        <i class="iconfont icon-view"></i> 345343
                    </div>
                    <div class="log-body">
                        <p>即便英国脱欧及美国总统大选结果等事件曾令人们大感意外，经济增长却未理会一些经济学家所发出的悲观警讯，在2017年有了一个极为亮眼的开局。</p>
                        <p>根据数据商Markit编制的报告，主要经济体的1月采购经理人指数（PMI）显示全球经济增长在今年初获得动力。摩根大通1月全球PMI指数从去年12月的53.6上升至53.9，为22个月高位，并且已经连续第五个月上升。</p>
                        <p>不过，海外舆论分析认为，全球经济增长能否得以存续，仍需关注今年余下时间来自欧洲多国大选等政治层面的格局变</p>
                        <strong>全球PMI一片“繁荣”</strong>
                        <p>在刚刚过去的1月，世界主要经济体都交出了亮眼的制造业及服务业成绩单。</p>
                        <p>美国供应管理协会（ISM）的数据显示，全国工厂活动指标触及2014年11月以来最高。指标高于50表明制造业扩张，制造业在美国经济中的占比约为12%。欧元区1月制造业活动增速则为近六年最快，制造业者信心创2012年中期以来最高。日本制造业活动扩张速度也创下近三年来最快。</p>
                        <p>而在服务业方面，美国1月Markit服务业PMI终值为55.6，创下2015年11月以来最高位。法国1月服务业PMI终值触及五年半高位，意大利1月Markit/ADACI服务业PMI也已经连续第八个月守在荣枯分水岭50上方。</p>
                        <p>中国的表现同样可圈可点。根据国家统计局的数据，今年1月，中国非制造业商务活动指数为54.6%，比上月微升0.1个百分点，保持平稳向好增长势头。中国制造业PMI为51.3%，比上月微落0.1个百分点，延续平稳扩张态势。其中，新出口订单指数和进口指数为50.3%和50.7%，分别高于上月0.2和0.4个百分点，均连续三个月位于临界点以上。</p>
                        <p>Markit的首席商业经济学家Chris Williamson分析指出：“令人鼓舞的是，一项针对未来产出的全球商业乐观程度调查数据触及20个月高位，发达市场和新兴市场被调查者对于今年的看法同时出现改善。”他表示：“一些细分数据表明，增长在2017年稍早可能得以加速。首先，新业务流入在全球范围内出现增长，增速为2014年9月以来最快，这在制造业及服务业同时发生。其次，企业薪资增速位于19个月高位。第三，积压工作分项指数创出22个月最大增幅。”</p>
                        <strong>通胀压力如影随形</strong>
                        <p>不过，强劲的全球经济增长仍然伴随着不断上升的通胀压力。Chris Williamson说，受燃料及能源等大宗商品价格上扬影响，全球平均投入成本在1月创下2011年6月以来最大月度涨幅。</p>
                        <p>在美国，ISM的最新数据显示，从铝材到瓦楞纸箱到钢材，美国制造商的原材料成本在全面攀升。在欧元区，1月份通胀率加速升至1.8%。在日本，投入价格加速攀升至22个月高点。</p>
                        <p>这已经对部分经济体产生影响。今年1月，英国整体PMI从去年12月的56.2下跌至54.5，创出三个月新低，而企业成本创下六年来最快增速。Markit表示，服务类公司在谈及成本压力上升的原因时，除了工资和运费外，还提到了英镑走软以及能源成本上升这两大因素。</p>
                        <p>“有趣之处在于，面对英国脱欧和美国总统特朗普上台等意外事件时，增长却出现加速。”不过，他也表示，这六个指标无法告诉我们有关未来几个月会发生什么的任何信息。全球经济增长是否会变得更强劲或是走向疲弱，这是一个开放的问题。</p>

